package model;

public class Prodi {
    private String id;
    private String namaProdi;

    public Prodi(String id, String namaProdi) {
        this.id = id;
        this.namaProdi = namaProdi;
    }

    public String getId() {
        return id;
    }

    public String getNamaProdi() {
        return namaProdi;
    }

    
}
